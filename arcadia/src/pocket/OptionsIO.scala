package arcadida.pocket

import chisel3._

class OptionsIO extends Bundle {
  /** Game index */
  val gameIndex = Input(UInt(32.W))
  /** Scaler mode */
  val scalerMode = Input(UInt(3.W))
  /** Enable debugging */
  val debug = Input(Bool())
  /** Layer enable */
  val layer = Input(Vec(3, Bool()))
  /** Sprite enable */
  val sprite = Input(Bool())
  /** Flip video output */
  val flip = Input(Bool())
  /** FM enable */
  val fm = Input(Bool())
  /** PCM enable */
  val pcm = Input(Bool())
}

object OptionsIO {
  def apply() = new OptionsIO
}
